fun main() {
    val a = 5
    val b = 2

    // Addition operator
    var c = a + b
    println(c)

    // Subtraction operator
    c = a - b
    println(c)

    // Multiplication operator
    c = a * b
    println(c)

    // Division operator
    c = a / b
    println(c)

    // Modulo operator
    c = a % b
    println(c)

    println(a.toDouble() / b)
    println(a.toDouble() % b.toDouble())
}